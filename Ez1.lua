local function applyVisualEffects(part)
    local startTime = tick()
    local timeElapsed = 0

    while timeElapsed <= 5 do
        local colorValue = (timeElapsed % 5) / 5
        part.Color = Color3.fromHSV(colorValue, 1, 1)
        part.Transparency = 0
        part.Material = Enum.Material.Neon
        wait()
        timeElapsed = tick() - startTime
    end
end

workspace.DescendantAdded:Connect(function(O)
    if O:IsA("BasePart") then
        spawn(function()
            applyVisualEffects(O)
        end)
    end
end)

for _, part in ipairs(workspace:GetDescendants()) do
    if part:IsA("BasePart") then
        spawn(function()
            applyVisualEffects(part)
        end)
    end
end

